
package com.mycompany.arrays;

public class DynamicArray {
    public static void main(String[] args) {
 
        // literal Array
    int num[] = new int[10];
    num[1] = 10;
    num[2] = 20;
    num[3] = 30;
    
        System.out.println(num[2]);
        
        // Dynamic array in java 
        num = new int[3];
        System.out.println(num[2]);
        
    }    
}
